export const getComentarioCongruente = (tituloReceta: string) => {
    // 1. Buscamos si la receta está en tu lista de comentarios
    const bloqueComentarios = misComentariosReales.find(item => item.receta === tituloReceta);

    // 2. Si existe, devolvemos uno de sus comentarios al azar
    if (bloqueComentarios) {
        return bloqueComentarios.comentarios[Math.floor(Math.random() * bloqueComentarios.comentarios.length)];
    }

    // 3. Si no existe (porque no le has escrito comentarios a todas las 70 recetas), devolvemos uno genérico
    const genericos = ["¡Increíble!", "Riquísimo.", "Fácil de hacer.", "A mi familia le encantó.", "La repetiré.", "Súper sana."];
    return genericos[Math.floor(Math.random() * genericos.length)];
};